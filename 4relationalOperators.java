 class relationalOperators {
    public static void main(String args[])
    {
        int x = 6;
        int y = 7;
        boolean result1 = x<y;
        boolean result2 = x>y;
        boolean result3 = x==y;
        boolean result4 = x!=y;
      
        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);
        System.out.println(result4);
      
    }
}
